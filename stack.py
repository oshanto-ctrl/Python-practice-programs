# stack = "Last in First out data structure"

my_stack = list()

# appending to stack
my_stack.append(1)
my_stack.append(5)
my_stack.append(10)
my_stack.append(11)

print(my_stack)

# poping from stack
print(my_stack.pop()) # O/P = 11
print(my_stack.pop()) # O/P = 10

print(my_stack) # O/P = [1, 5]

